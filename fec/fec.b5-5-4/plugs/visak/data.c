// Load library scope helper method definition
PiApiResult_t
VisakSignOff(PiSession_t *sess)
{

	PiSessionVerify(sess);
	appData_t *context = (appData_t*)sess->pub.context->data;
	
	char signoff[] = { DLE(context), EOT(context) };
	
	// Inform POS the link is disconnecting
	if ( ! PiSessionAtEof(sess) )
		PiPosSend(sess, signoff, sizeof(signoff));

	return (eDisconnect);
}								// PiApiResult_t VisakSignOff(PiSession_t *)

PiApiResult_t
VisakSendPos(PiSession_t *sess, char *rsp, int len)
{
	char *buf = alloca(4 + len);
	char *bp = buf;

	PiSessionVerify(sess);
	appData_t *context = (appData_t*)sess->pub.context->data;
	
	char *last = (context) ? context->lastResponse : 0;

	// Clear local stack buffer
	memset(buf, 0, 4 + len);

	// Build POS response packet
	*bp++ = STX(context);
	memcpy(bp, rsp, len);
	bp += len;
	*bp++ = ETX(context);
#if 1							// Set to zero (0) to test NAK behavior
	*bp = CalcLrc(buf + 1, 1 + len);	// include ETX in LRC
#else
	*bp = CalcLrc(buf + 1, len);	// LRC ERROR for NAK test
#endif
	len += 3;

	// Write POS response
	if (len != PiPosSend(sess, buf, len))
	{
		PiException(LogError, "PiPosSend", eFailure, "failed");
		return (VisakSignOff(sess));
	}							// if (len != PiPosSend(sess,buf,len))

	// Save response for NAK processing
	if (!(last))
	{
		// Subtract the STX, ETX, LRC counts from len
		len -= 3;

		// Save the "last" response for possible retransmission
		if ((context->lastResponse = (char *)malloc(1 + len)))
		{
			context->lastLen = len;
			sprintf(context->lastResponse, "%s", rsp);

			// Log the malloc() out-of-memory error
		}
		else
		{
			PiException(LogError, "malloc", eFailure, "failed");
		}						// if ((context->lastResponse = (char *)malloc(1+len)))

		// Only response to NAK once, this is after all TCP/IP communications
	}
	else
	{
		int len = context->lastLen;

		// PCI clear the response and release if from memory
		// NOTE: write bit patterns 10101010, 01010101, 11111111, 00000000
		memset(last, 0xAA, len);
		memset(last, 0x55, len);
		memset(last, 0xFF, len);
		memset(last, 0x00, len);

		// Remove the last response for appl plugin object
		free(context->lastResponse);
		context->lastResponse = 0;
	}							// if (!(last))

	return (eOk);
}								// PiApiResult_t VisakSendPos(PiSession_t *,char *,int)
